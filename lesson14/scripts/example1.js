function calculator(num1, num2, operation) {
    if (operation === "+") {
        console.log(num1 + num2);
    } else if (operation === "-") {
        console.log(num1 - num2)
    } else if (operation === "*") {
        console.log(num1 * num2)
    } else if (operation === "/") {
        console.log(num1 / num2)
    } else {
        console.log("Sorry, I don't know that symbol(s)")
    }
}

calculator(5,4, "+")
calculator(5,4, "+")
calculator(5,4, "+")